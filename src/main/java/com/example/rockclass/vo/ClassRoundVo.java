package com.example.rockclass.vo;

public class ClassRoundVo {
    Long id;

    Byte enrollNum;

    Byte classSerial;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Byte getEnrollNum() {
        return enrollNum;
    }

    public void setEnrollNum(Byte enrollNum) {
        this.enrollNum = enrollNum;
    }

    public Byte getClassSerial() {
        return classSerial;
    }

    public void setClassSerial(Byte classSerial) {
        this.classSerial = classSerial;
    }
}
